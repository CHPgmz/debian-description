            <h2>¿Qué es Debian?</h2>
            <p>El proyecto Debian fue fundado en el año 1993 por Ian Murdock, después de haber estudiado en la Universidad de Purdue. Él escribió el manifiesto de Debian, que utilizó como base para la creación de la distribución Linux Debian. Dentro de este texto, los puntos destacables son: mantener la distribución de manera abierta, coherente al espíritu del núcleo Linux y de GNU. </p>
            <p>Aproximadamente cada año desde que se inició el programa se ha publicado una versión actualizada con nuevas características, colocando el ritmo de publicación desde el cambio de siglo en unos 24 meses. Además, para evitar confusiones, la primera versión oficial fue 1.1 y no 1.0 después de que, por error, un fabricante publicara una versión inédita llamada 1.0. Para designar dicha versión del software se le dio el nombre Buzz, uno de los personajes de la película animada Toy Story. El por qué es bastante simple: en ese momento, Bruce Perens, la persona que se hizo cargo de la línea del proyecto Debian, había trabajado como desarrollador de herramientas para el estudio Pixar (que es el estudio de cine creador de Toy Story).</p>
